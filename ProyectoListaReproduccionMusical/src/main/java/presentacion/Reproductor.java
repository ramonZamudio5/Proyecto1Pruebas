   private void cargarListaReproduccion(List<Cancion> canciones) {
        jPanel2.removeAll(); 
        jPanel2.setLayout(new GridLayout(0, 2, 10, 5)); 

        for (Cancion cancion : canciones) {
            JLabel labelCancion = new JLabel("  "+cancion.getTitulo());
            JLabel labelArtista = new JLabel(cancion.getArtista());
            jPanel2.add(labelCancion);
            jPanel2.add(labelArtista);
        }

        jPanel2.revalidate(); 
        jPanel2.repaint();    
    }
   
   private void cargaCancionActual(){
       jLabel3.setText(canciones.getFirst().getTitulo());
       jLabel4.setText(canciones.getFirst().getArtista());
       canciones.removeFirst();
       cargarListaReproduccion(canciones);
   }

